"""
eq - равно (==)
ne - не равно (!=)
gt - больше (>)
lt - меньше (<)
ge - больше или равно (>=)
le - меньше или равно (<=)
"""

a = 3
b = 2

eq = a == b
print(f"{a} = {b} is {eq}")

ne = a != b
print(f"{a} != {b} is {ne}")

gt = a > b
print(f"{a} > {b} is {gt}")

lt = a < b
print(f"{a} < {b} is {lt}")

ge = a >= b
print(f"{a} >= {b} is {ge}")

le = a <= b
print(f"{a} <= {b} is {le}")
