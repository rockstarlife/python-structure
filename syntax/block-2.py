"""
Арифметические: +, -, *, / (деление), // (целое), % (остаток), ** (степень).
sum - сложение (+)
diff - вычитание (-)
prod - умножение (*)
div - деление (/)
floor - целочисленное деление (//)
mod - остаток от деления (%)
pow - возведение в степень (**)
"""

a = 3
b = 2

sum = a + b
print(f"sum = {sum}")

diff = a - b
print(f"diff = {diff}")

prod = a * b
print(f"prod = {prod}")

div = a / b
print(f"div = {div}")

floor = a // b
print(f"floor = {floor}")

mod = a % b
print(f"mod = {mod}")

pow = a**b
print(f"pow = {pow}")

